    private boolean authenticateUser(String username, char[] password) {
        Map<Integer, Map<String, Object>> users = model.getUsersData();

        for (Map<String, Object> user : users.values()) {
            String storedUsername = (String) user.get("usuario");
            String storedPassword = (String) user.get("pwd");

            if (storedUsername.equals(username) && storedPassword.equals(new String(password))) {
                return true;
            }
        }

        return false;
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String enteredUsername = jTextField1.getText();
        char[] enteredPassword = jPasswordField1.getPassword();

        if (authenticateUser(enteredUsername, enteredPassword) ||  (enteredUsername.equals("admin") && new String(enteredPassword).equals("administrador"))) {
            if (enteredUsername.equals("admin")) {
                Menu menu = new Menu(model);
                menu.setVisible(true);
            
            } else {
                Prestamo prestamo = new Prestamo(model, enteredUsername);
                prestamo.setVisible(true);
            }
        
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(null, "Sesi칩n incorrecta.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

